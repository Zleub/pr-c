struct TkStubs	*manage_TkStubs(int macro)
{
	static struct TkStubs	*current;

	if (macro == NEW)
		current = new_TkStubs();
	else if (macro == GET)
		return (current);
	else
		write(1, "manage_TkStubs: no decent macro\n", 32);
	return (NULL);
}
